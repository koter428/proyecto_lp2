    public void habilitar(int estado) {
        switch (estado) {
            case 0://deshabilitar los botones
                btnagregar.setEnabled(false);
                btneditar.setEnabled(false);
                btnborrar.setEnabled(false);
                btncancelar.setEnabled(true);
                btngrabar.setEnabled(false);
                btnsalir.setEnabled(false);
                btnimprimir.setEnabled(false);
                break;
            case 1: //habilitar los botones
                btnagregar.setEnabled(true);
                btneditar.setEnabled(true);
                btnborrar.setEnabled(true);
                btncancelar.setEnabled(false);
                btngrabar.setEnabled(false);
                btnsalir.setEnabled(true);
                btnimprimir.setEnabled(true);
               llenar_grilla();
                break;
               
        }
    }

    public void llenar_combo(String ordenacion) {
       
        rs1 = con.listar_datos("select(mar_cod ||'-'||mar_descri)"+"as marca from marca order by mar_cod="+ordenacion+"desc");
        try {
            while (rs1.isBeforeFirst()) {
                while(rs1.next()){
                   mar_cod.addItem(rs1.getString("marca"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public  void llenar_combo1(String ordenacion) {
        rs1 = con.listar_datos("select(tipo_cod ||'-'||tipo_descri)"+"as tipo_impuesto from tipo_impuesto order by tipo_cod="+ordenacion+"desc");
        try {
            while (rs1.isBeforeFirst()) {
                while(rs1.next()){
                   imp_cod.addItem(rs1.getString("tipo_impuesto"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void llenar_grilla() {
      String campos[]= new String[]{"CODIGO","MARCA","DESCRIPCION","PRECIO VEN","PRECIO COM", "IMPUESTO"};
      DefaultTableModel modelo =new DefaultTableModel(null,campos);
      grilla.setModel(modelo);
      rs=con.listar_datos("select * from articulo order by art_cod");
        try {
            while (rs.next()) {
                String datos[]=  new String[]{rs.getString("art_cod"),rs.getString("art_descri")+" "+rs.getString("mar_descri"),rs.getString("art_precioc"),
                    rs.getString("art_preciov"),rs.getString("tipo_descri")};
                modelo.addRow(datos);
                
            }
        } catch (Exception e) {
        }
   }
