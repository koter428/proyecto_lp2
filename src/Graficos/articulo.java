    private void cod_barraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cod_barraKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!cod_barra.getText().trim().equals("")) {
                if (Integer.parseInt(cod_barra.getText()) > 0) {
                    btngrabar.setEnabled(true);
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atención", JOptionPane.INFORMATION_MESSAGE);
                    cod_barra.setText("");
                    cod_barra.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                cod_barra.setText("");
                cod_barra.requestFocus();
            }
        }
    }//GEN-LAST:event_cod_barraKeyPressed

    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(null, "Desea grabar los datos de la cliente?", "Atención", JOptionPane.YES_NO_OPTION);
        if (mensaje == JOptionPane.YES_OPTION) {
            if (ban == 1) { //es agregar la accion
                con.insertar_datos("articulo", "art_cod, art_codbarra, mar_cod, art_descri, art_precioc, art_preciov, tipo_cod",
                         art_cod.getText() + ",'" + cod_barra.getText() + "',split_part('"+mar_cod.getSelectedItem() +"','-',1)::integer,'"
                        + art_descripcion.getText() + "',"
                                 + art_pcompra.getText() + ","
                                 +art_pventa.getText()
                                 +",split_part('"+imp_cod.getSelectedItem() +"','-',1)::integer", 0);
                btncancelar.doClick();
            }
            
            if (ban == 2) {//accion editar
                con.actualizar_datos("articulo","art_codbarra="+cod_barra.getText()
                        +",art_descri='"+art_descripcion.getText()
                        +"',art_precioc='"+art_pcompra.getText()+"',art_preciov='"
                        +art_pventa.getText()+"'", "art_cod="+art_cod.getText());
                btncancelar.doClick();
            }
        } else {
            btncancelar.doClick();
        }

    }//GEN-LAST:event_btngrabarActionPerformed

