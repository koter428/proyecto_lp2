    private void art_codKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_art_codKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!art_cod.getText().trim().equals("")) {
                if (Integer.parseInt(art_cod.getText().trim()) > 0) {
                    rs2 = con.listar_datos("select * from articulo where art_cod =" + art_cod.getText());
                    try {
                        if (rs2.isBeforeFirst()) {
                            rs2.next();
                            cod_barra.setText(rs2.getString("art_codbarra"));
                  
                            art_descripcion.setText(rs2.getString("art_descri"));
                            art_pcompra.setText(rs2.getString("art_precioc"));
                            art_pventa.setText(rs2.getString("art_preciov"));
                            
                            
                    
                            if (ban == 3) {
                                int mensaje = JOptionPane.showConfirmDialog(null, "Desea borrar los datos del cliente?",
                                        "Atención", JOptionPane.YES_NO_OPTION);
                                if (mensaje == 0) {
                                    con.BorrarDatos("articulo", "art_cod", art_cod.getText());
                                    btncancelar.doClick();
                                } else {
                                    btncancelar.doClick();
                                }
                            } else {
                                cod_barra.setEnabled(true);
                                cod_barra.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontraron datos coincidentes", "Atención", JOptionPane.INFORMATION_MESSAGE);
                            art_cod.setText("");
                            art_cod.requestFocus();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atención", JOptionPane.INFORMATION_MESSAGE);
                    art_cod.setText("");
                    art_cod.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                art_cod.setText("");
                art_cod.requestFocus();
            }
        }
    }//GEN-LAST:event_art_codKeyPressed

