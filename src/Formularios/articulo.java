    private void vart_codbarraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vart_codbarraKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!vart_codbarra.getText().trim().equals("")) {
                vmar_cod.setEnabled(true);
                vmar_cod.requestFocus();
            btngrabar.setEnabled(true);
            
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                vart_codbarra.setText("");
                vart_codbarra.requestFocus();
            }
        }
    }//GEN-LAST:event_vart_codbarraKeyPressed

