    public void habilitar(int estado) {
        switch (estado) {
            case 0:
                btnagregar.setEnabled(false);
                btneditar.setEnabled(false);
                btnborrar.setEnabled(false);
                btngrabar.setEnabled(false);
                btncancelar.setEnabled(true);
                btnsalir.setEnabled(false);
                btnimprimir.setEnabled(false);
                break;
            case 1:
                btnagregar.setEnabled(true);
                btneditar.setEnabled(true);
                btnborrar.setEnabled(true);
                btngrabar.setEnabled(false);
                btncancelar.setEnabled(false);
                btnsalir.setEnabled(true);
                btnimprimir.setEnabled(true);
                break;
        }
    }

    public void llenar_combo(String ordenacion) {
        rs1 = con.listar_datos("select (mar_cod||'-'||mar_descri) as descri "
                + "from marca order by mar_cod =" + ordenacion + " desc");
        try {
            while (rs1.next()) {
                vmar_cod.addItem(rs1.getString("descri"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void llenar_combo1(String ordenacion) {
        rs2 = con.listar_datos("select (tipo_cod||'-'||tipo_descri) as tipo "
                + "from tipo_impuesto order by tipo_cod =" + ordenacion + " desc");
        try {
            while (rs2.next()) {
                vtipo_cod.addItem(rs2.getString("tipo"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void llenar_grilla(){
        String campos[] = new String[]{"CODIGO","DESCRIPCION","PRECIO V.","PRECIO C.","IMPUESTO"};
        DefaultTableModel modelo = new DefaultTableModel(null,campos);
        grilla.setModel(modelo);
        rs=con.listar_datos("select * from v_articulo order by art_cod");
        try {
            while (rs.next()) {
                String datos[]= new String[]{rs.getString("art_cod"),rs.getString("art_descri")+" "+rs.getString("mar_descri"),
                rs.getString("art_precioc"),rs.getString("art_preciov"),rs.getString("tipo_descri")};
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
