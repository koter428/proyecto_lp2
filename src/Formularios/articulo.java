    private void vart_codKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vart_codKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!vart_cod.getText().trim().equals("")) {
                if (Integer.parseInt(vart_cod.getText().trim()) > 0) {
                    rs = con.listar_datos("select * from articulo where art_cod =" + vart_cod.getText());
                    try {
                        if (rs.isBeforeFirst()) {
                            rs.next();
                            vart_codbarra.setText(rs.getString("art_codbarra"));
                            llenar_combo(rs.getString("mar_cod"));
                            llenar_combo1(rs.getString("tipo_cod"));
                            vart_descri.setText(rs.getString("art_descri"));
                            
                            vart_precioc.setText(rs.getString("art_precioc"));
                            vart_preciov.setText(rs.getString("art_preciov"));
                           vart_descri.setEnabled(true);
                           vart_descri.requestFocus();
                           vart_precioc.setEnabled(true);
                            vart_preciov.setEnabled(true);
                            vtipo_cod.setEnabled(true);
                            vmar_cod.setEnabled(true);
                          
                            
                    
                            if (ban == 3) {
                                int mensaje = JOptionPane.showConfirmDialog(null, "Desea borrar los datos del cliente?",
                                        "Atención", JOptionPane.YES_NO_OPTION);
                                if (mensaje == 0) {
                                    con.BorrarDatos("articulo", "art_cod", vart_cod.getText());
                                    btncancelar.doClick();
                                } else {
                                    btncancelar.doClick();
                                }
                            } else {
                                vart_codbarra.setEnabled(true);
                                vart_codbarra.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontraron datos coincidentes", "Atención", JOptionPane.INFORMATION_MESSAGE);
                            vart_cod.setText("");
                           vart_cod.requestFocus();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Graficos.articulo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atención", JOptionPane.INFORMATION_MESSAGE);
                    vart_cod.setText("");
                    vart_cod.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                vart_cod.setText("");
                vart_cod.requestFocus();
            }
        }
    }                                  

