public void habilitar(int estado) {
        switch (estado) {
            case 0:
                btnagregar.setEnabled(false);
                btneditar.setEnabled(false);
                btnborrar.setEnabled(false);
                btngrabar.setEnabled(false);
                btncancelar.setEnabled(true);
                btnsalir.setEnabled(false);
                btnimprimir.setEnabled(false);
                break;
            case 1:
                btnagregar.setEnabled(true);
                btneditar.setEnabled(true);
                btnborrar.setEnabled(true);
                btngrabar.setEnabled(false);
                btncancelar.setEnabled(false);
                btnsalir.setEnabled(true);
                btnimprimir.setEnabled(true);
                break;
        }
}
              public void llenar_combo(String ordenacion) {
        rs1 = con.listar_datos("select (dep_cod||'-'||art_cod) as art_cod "
                + "from stock order by dep_cod =" + ordenacion + " art_cod");
        try {
            while (rs1.next()) {
                art_cod.addItem(rs1.getString("cantidad_minima"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }     
       public void llenar_combo1(String ordenacion) {
        rs2 = con.listar_datos("select (dep_cod||'-'||stoc_cant) as stoc_cant "
                + "from stock order by dep_cod =" + ordenacion + " art_cod");
        try {
            while (rs2.next()) {
                stoc_cant.addItem(rs2.getString("cantidad_minima"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }       
         public void llenar_grilla() {
        String campos[] = new String[]{"CODIGO", "ARTICULO", "CANTIDAD MINIMA.","STOCK CANTIDAD"};
        DefaultTableModel modelo = new DefaultTableModel(null, campos);
        grilla.setModel(modelo);
        rs = con.listar_datos("select * from stock order by dep_cod");
        try {
            while (rs.next()) {
                String datos[] = new String[]{rs.getString("art_cod"), rs.getString("art_cod") + " " + rs.getString("cantidad_minima"),
                    rs.getString("stoc_cant"),rs.getString("stoc_cant")};
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(articulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
