    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(null, "Desea grabar los datos del stock?", "Atención", JOptionPane.YES_NO_OPTION);
        if (mensaje == JOptionPane.YES_OPTION) {
            if (ban == 1) {
                con.insertar_datos("stock", "dep_cod, art_cod, cant_minima, stoc_cant",
                    dep_cod.getText() + ",'" + cant_minima.getText() + "',split_part('" + art_cod.getSelectedItem()
                    + "','-',1)::integer,'" + cant_minima.getText() + "'," + stoc_cant.getSelectedItem() + "','-',1)::integer", 0);
                btncancelar.doClick();
            }
            if (ban == 2) {
                con.actualizar_datos("stock", "dep_cod='" + cant_minima.getText()
                    + "',mar_cod=split_part('" + art_cod.getSelectedItem() + "','-',1)::integer,cant_minima='"
                    + cant_minima.getText() + "',dep_cod=" + cant_minima.getText()
                    + ",dep_cod=" + cant_minima.getText()
                    + ",stoc_cant=split_part('" + stoc_cant.getSelectedItem() + "','-',1)::integer", "art_cod=" + dep_cod.getText());
                btncancelar.doClick();
            }
        } else {
            btncancelar.doClick();
        }
    }//GEN-LAST:event_btngrabarActionPerformed

    private void dep_codKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dep_codKeyPressed
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!dep_cod.getText().trim().equals("")) {
                dep_cod.setEnabled(true);
                dep_cod.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                cant_minima.setText("");
                cant_minima.requestFocus();
            }
        }
        
    }//GEN-LAST:event_dep_codKeyPressed

