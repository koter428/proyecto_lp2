    private void vcli_codKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vcli_codKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!vcli_cod.getText().trim().equals("")) {
                if (Integer.parseInt(vcli_cod.getText().trim()) > 0) {
                    rs2 = con.listar_datos("select * from empleado where emp_cod =" + vcli_cod.getText());
                    try {
                        if (rs2.isBeforeFirst()) {
                            rs2.next();
                            vcli_ruc.setText(rs2.getString("car_cod"));
                            vcli_nombre.setText(rs2.getString("emp_nombre"));
                            vcli_apellido.setText(rs2.getString("emp_apellido"));
                            vcli_direccion.setText(rs2.getString("emp_direcc"));
                            em_nick.setText(rs2.getString("emp_nick"));
                            emp_contra.setText("emp_clave");
                            
                            vcli_tel.setText(rs2.getString("emp_tel"));
                            
                            if (ban == 3) {
                                int mensaje = JOptionPane.showConfirmDialog(null, "Desea borrar los datos del cliente?",
                                        "Atención", JOptionPane.YES_NO_OPTION);
                                if (mensaje == 0) {
                                    con.BorrarDatos("empleado", "car_cod", vcli_cod.getText());
                                    btncancelar.doClick();
                                } else {
                                    btncancelar.doClick();
                                }
                            } else {
                                vcli_ruc.setEnabled(true);
                                vcli_ruc.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontraron datos coincidentes", "Atención", JOptionPane.INFORMATION_MESSAGE);
                            vcli_cod.setText("");
                            vcli_cod.requestFocus();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(empleado.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atención", JOptionPane.INFORMATION_MESSAGE);
                    vcli_cod.setText("");
                    vcli_cod.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                vcli_cod.setText("");
                vcli_cod.requestFocus();
            }
        }
    }//GEN-LAST:event_vcli_codKeyPressed

