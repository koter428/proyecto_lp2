    public void habilitar(int estado) {
        switch (estado) {
            case 0://deshabilitar los botones
                btnagregar.setEnabled(false);
                btneditar.setEnabled(false);
                btnborrar.setEnabled(false);
                btncancelar.setEnabled(true);
                btngrabar.setEnabled(false);
                btnsalir.setEnabled(false);
                btnimprimir.setEnabled(false);
                break;
            case 1: //habilitar los botones
                btnagregar.setEnabled(true);
                btneditar.setEnabled(true);
                btnborrar.setEnabled(true);
                btncancelar.setEnabled(false);
                btngrabar.setEnabled(false);
                btnsalir.setEnabled(true);
                btnimprimir.setEnabled(true);
                break;
        }
    }

    public void llenar_grilla() {
        String campos[] = new String[]{"CODIGO", "CARGO","NOMBRE"," APELLIDO"};
        DefaultTableModel modelo = new DefaultTableModel(null, campos);
        grilla.setModel(modelo);
        rs1 = con.listar_datos("select * from empleado order by emp_cod");
        try {
            while (rs1.next()) {
                String datos[] = new String[]{rs1.getString("emp_cod"), rs1.getString("car_cod"),
                rs1.getString("emp_nombre"),rs1.getString("emp_apellido")};
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(empleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

