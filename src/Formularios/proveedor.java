    private void vprv_codKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_vprv_codKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!vprv_cod.getText().trim().equals("")) {
                if (Integer.parseInt(vprv_cod.getText().trim()) > 0) {
                    rs2 = con.listar_datos("select * from proveedor where prv_cod =" + vprv_cod.getText());
                    try {
                        if (rs2.isBeforeFirst()) {
                            rs2.next();
                            vprv_ruc.setText(rs2.getString("prv_ruc"));
                            vprv_razonsocial.setText(rs2.getString("prv_razonsocial"));
                            vprv_direccion.setText(rs2.getString("prv_direccion"));
                            vprv_telefono.setText(rs2.getString("prv_telefono"));
                            rs2.close();
                            vprv_ruc.setEnabled(true);
                            vprv_razonsocial.setEnabled(true);
                            vprv_direccion.setEnabled(true);
                            vprv_telefono.setEnabled(true);
                            btngrabar.setEnabled(true);
                            vprv_cod.setEnabled(false);
                            if (ban == 3) {
                                int mensaje = JOptionPane.showConfirmDialog(null, "Desea borrar los datos del campo?", "Atencion", JOptionPane.YES_NO_OPTION);
                                if (mensaje == 0) {
                                    con.BorrarDatos("proveedor", "prv_cod", vprv_cod.getText());
                                    btncancelar.doClick();
                                } else {
                                    btncancelar.doClick();
                                }
                            }/*else{
                                vcli_cod.setEnabled(false);
                                vcar_descri.setEnabled(true);
                                vcar_descri.selectAll();
                                vcar_descri.requestFocus();
                            }*/
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontraron datos coincidentes", "Atencion", JOptionPane.INFORMATION_MESSAGE);
                            vprv_cod.setText("");
                            vprv_cod.requestFocus();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(proveedor.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atencion", JOptionPane.INFORMATION_MESSAGE);
                    vprv_cod.setText("");
                    vprv_cod.requestFocus();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede quedar vacio", "Atencion", JOptionPane.INFORMATION_MESSAGE);
                vprv_cod.setText("");
                vprv_cod.requestFocus();
            }
        }
    }//GEN-LAST:event_vprv_codKeyPressed

