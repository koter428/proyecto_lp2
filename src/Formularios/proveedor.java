    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        habilitar(0);
        ban = 3;
        JTextField[] tfParam = new JTextField[5];
        tfParam[0] = this.vprv_cod;
        tfParam[1] = this.vprv_ruc;
        tfParam[2] = this.vprv_razonsocial;
        tfParam[3] = this.vprv_direccion;
        tfParam[4] = this.vprv_telefono;
        VentanaBuscar buscar = new VentanaBuscar("select prv_cod, prv_ruc,prv_razonsocial,prv_direccion, prv_telefono from proveedor"
                + " where (prv_cod||''||prv_ruc||''||prv_razonsocial||''||prv_direccion||''||prv_telefono) ilike ",
                new String[]{"CODIGO", "RUC_CI", "RAZON SOCIAL", "DIRECCION", "TELEFONO"}, 5, tfParam);

        buscar.setTitle("Buscador clientes");
        buscar.setVisible(true);
        if (ban == 3) {
            int mensaje = JOptionPane.showConfirmDialog(null, "Desea Borrar los datos del proveedor", "Atencion", JOptionPane.YES_NO_OPTION);
            if (mensaje == 0) {
                con.BorrarDatos("proveedor", "prv_cod", vprv_cod.getText());
                btncancelar.doClick();
            } else {
                btncancelar.doClick();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

