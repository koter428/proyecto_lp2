    public void llenar_grilla() {
        String campos[] = new String[]{"COD.", "RUC", "RAZON SOCIAL", "DIRECCION", "TELEFONO"};
        DefaultTableModel modelo = new DefaultTableModel(null, campos);
        grillacli.setModel(modelo);
        rs1 = con.listar_datos("select * from proveedor order by prv_cod");
        try {
            while (rs1.next()) {
                String datos[] = new String[]{rs1.getString("prv_cod"), rs1.getString("prv_ruc"), rs1.getString("prv_razonsocial"),
                    rs1.getString("prv_direccion"), rs1.getString("prv_telefono")};
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(proveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public static boolean esSoloLetras(String cadena) {
        //Recorremos cada caracter de la cadena y comprobamos si son letras.
        //Para comprobarlo, lo pasamos a mayuscula y consultamos su numero ASCII.
        //Si está fuera del rango 65 - 90, es que NO son letras.
        //Para ser más exactos al tratarse del idioma español, tambien comprobamos
        //el valor 165 equivalente a la Ñ
        for (int i = 0; i < cadena.length(); i++) {

            char caracter = cadena.toUpperCase().charAt(i);

            int valorASCII = (int) caracter;

            if (valorASCII != 165 && (valorASCII < 65 || valorASCII > 90)) {
                return false; //Se ha encontrado un caracter que no es letra
            }
        }

        //Terminado el bucle sin que se haya retornado false, es que todos los caracteres son letras
        return true;

    }

    public static boolean esnumero(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

