    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(null, "Desea grabar la compra?", "Atención", JOptionPane.YES_NO_OPTION);
        if (mensaje == JOptionPane.YES_OPTION) {
            if (ban == 1) {
                con.insertar_datos("compras", "ccom_cod, emp_cod, pvri_cod, com_fecha, tipo_compra, can_cuota, prv_plazo, ccom_total, ccom_estado",
                    com_cod.getText() + "," + empleado + "," + prvi_cod.getText() + ",'" + con.obtener_fecha(com_fecha.getDate()) + "','"
                    + ctipo_compra.getSelectedItem() + "'," + vcan_cuota.getText() + "," + prv_plazo.toString()
                    + "," + ccom_cod.getText() + ",'P'", 0);
                for (int i = 0; i < grilla.getRowCount(); i++) {
                    con.insertar_datos("detalle_ventas", "ven_cod, dep_cod, art_cod, ven_cant, ven_precio, exenta, iva_5, iva_10",
                        com_cod.getText() + ",split_part('" + grilla.getValueAt(i, 5) + "','-',1)::integer,"
                        + grilla.getValueAt(i, 0) + "," + grilla.getValueAt(i, 2) + "," + grilla.getValueAt(i, 3) + ",0,0,0", 1);
                }
                if (cpedido.getSelectedItem().toString().equals("CON PEDIDO")) {
                    if (!cped_cod.getText().trim().equals("")) {
                        con.insertar_datos("pedido_compra", "ped_cod,com_cod,obs_pedido", cped_cod.getText()
                            +","+com_cod.getText()+",''", 1);
                    }
                }

                btncancelar.doClick();
            }
        } else {
            btncancelar.doClick();
        }
    }//GEN-LAST:event_btngrabarActionPerformed

