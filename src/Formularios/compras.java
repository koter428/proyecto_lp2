    private void ccantKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ccantKeyPressed
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) grilla.getModel();
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!ccant.getText().trim().equals("")) {
                if (Integer.parseInt(ccant.getText().trim()) > 0) {
                    rs2 = con.listar_datos("select * from c_articulo where cart_cod=" + cart_cod.getText());
                    try {
                        if (rs2.isBeforeFirst()) {
                            if (grilla.getRowCount() == 0) {
                                //esta vacio
                                rs2.next();
                                String[] datosdetalle = new String[]{rs2.getString("cart_cod"),
                                    rs2.getString("cart_descri") + " " + rs2.getString("mar_descri"),
                                    ccant.getText(), rs2.getString("ccom_precio"),
                                    String.valueOf(Integer.parseInt(ccant.getText())
                                        * Integer.parseInt(rs2.getString("ccom_precio"))),
                                    cdep_cod.getSelectedItem().toString()};
                                modelo.addRow(datosdetalle);
                                rs2.close();
                                /*total_compras*/ 
                            } else {
                                //validar si existe el detalle
                                int lugar = 0;
                                String valor = "";
                                boolean encontro = false;
                                for (int i = 0; i < grilla.getRowCount(); i++) {
                                    if (cart_cod.getText().trim().equals(grilla.getValueAt(i, 0))
                                        && cdep_cod.getSelectedItem().toString().equals(grilla.getValueAt(i, 5))) {
                                        lugar = i;
                                        valor = String.valueOf(grilla.getValueAt(i, 2));
                                        encontro = true;
                                    }
                                }
                                if (encontro) {
                                    modelo.removeRow(lugar);
                                    rs2.next();
                                    String[] datosdetalle = new String[]{rs2.getString("art_cod"),
                                        rs2.getString("art_descri") + " " + rs2.getString("mar_descri"),
                                        String.valueOf(Integer.parseInt(ccant.getText()) + Integer.parseInt(valor)),
                                        rs2.getString("art_preciov"),
                                        String.valueOf(Integer.parseInt(ccant.getText())
                                            * Integer.parseInt(rs2.getString("art_preciov"))),
                                        cdep_cod.getSelectedItem().toString()};
                                    modelo.addRow(datosdetalle);
                                    rs2.close();
                                    /*total_compras*/
                                } else {
                                    rs2.next();
                                    String[] datosdetalle = new String[]{rs2.getString("art_cod"),
                                        rs2.getString("art_descri") + " " + rs2.getString("mar_descri"),
                                        ccant.getText(), rs2.getString("art_preciov"),
                                        String.valueOf(Integer.parseInt(ccant.getText())
                                            * Integer.parseInt(rs2.getString("art_preciov"))),
                                        cdep_cod.getSelectedItem().toString()};
                                    modelo.addRow(datosdetalle);
                                    rs2.close();
                                    /*total_compras*/
                                }
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "No se encontraron registros coincidentes", "Atención", JOptionPane.INFORMATION_MESSAGE);
                            ccant.setText("");
                            ccant.requestFocus();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(ventas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Este campo debe ser mayor a cero", "Atención", JOptionPane.INFORMATION_MESSAGE);
                    ccant.setText("");
                    ccant.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este campo no puede estar vacio", "Atención", JOptionPane.INFORMATION_MESSAGE);
                ccant.setText("");
                ccant.requestFocus();
            }
            cart_descri.setText("");
            cart_cod.setText("");
            ccant.setText("");
            ccom_precio.setText("");
            cdep_cod.requestFocus();
        }
    }//GEN-LAST:event_ccantKeyPressed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        habilitar(0);
        ban = 1;//agregar
        llenar_combo();
        rs1 = con.listar_datos("select coalesce(max(prv_cod),0)+1 as ultimo,"
            + "current_date as fecha from proveedor");
        rs2 = con.listar_datos("select (prv_cod||' '||prv_ruc) as nombres "
            + "from proveedor where prv_cod=" + empleado);
        try {
            if (rs1.isBeforeFirst()) {
                rs1.next();
                com_cod.setText(rs1.getString("ultimo"));
                strfecha = rs1.getString("fecha");
                fecha = formatofecha.parse(strfecha);
                com_fecha.setDate(fecha);
                rs1.close();
                rs2.next();
                proveedor.setText(rs2.getString("nombres"));
                rs2.close();
                ctipo_compra.setEnabled(true);
                ctipo_compra.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ventas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ventas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

